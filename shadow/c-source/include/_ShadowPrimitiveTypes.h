#ifndef SHADOW_PRIMITIVE_TYPES_H
#define SHADOW_PRIMITIVE_TYPES_H

#include <stdbool.h>
#include <stdint.h>

// boolean
typedef bool shadow_boolean_t;

// byte
typedef int8_t shadow_byte_t;

// ubyte
typedef uint8_t shadow_ubyte_t;

// short
typedef int16_t shadow_short_t;

// ushort
typedef uint16_t shadow_ushort_t;

// int
typedef int32_t shadow_int_t;

// uint
typedef uint32_t shadow_uint_t;

// code
typedef int32_t shadow_code_t;

// long
typedef int64_t shadow_long_t;

// ulong
typedef uint64_t shadow_ulong_t;

// float
typedef float shadow_float_t;

// double
typedef double shadow_double_t;

#endif